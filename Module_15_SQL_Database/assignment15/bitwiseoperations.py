a = 5
b = 3

result_and = a & b
print(f"The bitwise & between {a} and {b} = {result_and}")

result_or = a | b
print(f"The bitwise | between {a} and {b} = {result_or}")

result_xor = a ^ b
print(f"The bitwise ^ between {a} and {b} = {result_xor}")


result_not = ~a
print(f"The bitwise ~ of {a} = {result_not}")

result_left_shift = a << 1
print(f"The bitwise left shift << of {a} = {result_left_shift}")

result_right_shift = a >> 1
print(f"The bitwise right shift >> of {a} = {result_right_shift}")
